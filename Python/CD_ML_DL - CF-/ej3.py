#   Sumando datos a un Array
import numpy as np

a = np.array([1, 2, 3, 4, 5])

#   Operaciones aritmeticas a un arreglo
print(f'Sumando a la variable (a) que es un arreglo: {a + 10}')
print(f'Restando a la variable (a) que es un arreglo: {a - 10}')
print(f'Multiplicando a la variable (a) que es un arreglo: {a * 10}')
print(f'Dividiendo a la variable (a) que es un arreglo: {a / 10}')