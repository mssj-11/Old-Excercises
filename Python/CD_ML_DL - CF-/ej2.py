#   Array con numpy
import numpy as np

a = np.array([1, 2, 3, 4, 5])

print(f'La variable (a) es un arreglo: {a}')
print(f'El tipo de datos es: {type(a)}')
print(f'La cantidad de elementos es de: {a.size}')
print(f'La dimension del arreglo son de: {a.ndim}')
print(f'El tipo de datos que almacena el arreglo es: {a.dtype}')
print(f'La dimension y cantidad de datos del arreglo es de: {a.shape}')