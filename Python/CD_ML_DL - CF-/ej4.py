#   Operaciones con 2 o mas Array
import numpy as np

a = np.array([1, 2, 3, 4, 5])
b = np.array([10, 18, 17, 16, 45])

#   Operaciones con 2 o mas arreglos
print(f'La SUMA de los arreglos es: {a + b}')
print(f'La RESTA de los arreglos es: {a - b}')
print(f'La MULTIPLICACION de los arreglos es: {a * b}')
print(f'La DIVISION de los arreglos es: {a / b}')