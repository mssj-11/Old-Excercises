package joss;

/**
 *
 * @author Daniel
 */
public class Figura {
    public int lados;
    public String nombre;
    public String tipo;

    public Figura(int lados, String nombre, String tipo) {
        this.lados = lados;
        this.nombre = nombre;
        this.tipo = tipo;
    }

    public int getLados() {
        return lados;
    }

    public void setLados(int lados) {
        this.lados = lados;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getTipo() {
        return tipo;
    }

    public void setTipo(String tipo) {
        this.tipo = tipo;
    }
    
    
    
}
