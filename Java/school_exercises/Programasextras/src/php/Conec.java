    private void INSERTARActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_INSERTARActionPerformed
        // TODO add your handling code here:
        try {     
            Connection co = getconec();
           
            PreparedStatement ps = co.prepareStatement("INSERT INTO alumnos(ID, Nombre, Grupo, Carrera)VALUES(?,?,?,?);");
            ps.setString(1, id1.getText());
            ps.setString(2, no1.getText());
            ps.setString(3, gru1.getText());
            ps.setString(4, car1.getText());
           
            int n = ps.executeUpdate();
            if (n > 0) {
                JOptionPane.showMessageDialog(null, "datos insertados");
                L();

            } else {
                JOptionPane.showMessageDialog(null, "datos no insertados");
                L();
            }

            
           
        } catch (Exception ex) {      
        }
    }//GEN-LAST:event_INSERTARActionPerformed

