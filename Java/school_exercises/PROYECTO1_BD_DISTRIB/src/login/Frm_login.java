    private void btentrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btentrarActionPerformed

        String busqueda_usuario = metodos.buscarUsuarioRegistrado(txtcorreo.getText(), txtcontraseña.getText());

        if (txtcorreo.getText().equals("root") && txtcontraseña.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "BIENVENIDO INICIASTE SESION COMO root (ADMINISTRADOR)");
            Frm_Sistema ventana = new Frm_Sistema();
            ventana.lbNombre.setText("ADMINISTRADOR");
            //mandar a llamar la sig ventana
            ventana.setVisible(true);
            this.dispose();

        } else if (busqueda_usuario.equals("USUARIO ENCONTRADO")) {
            String busqueda_nombre = metodos.buscarNombre(txtcorreo.getText());
            JOptionPane.showMessageDialog(this, "BIENVENIDO \n" + busqueda_nombre);
//MOSTRAR USUARIO EN UN LABEL PUBLICO
            Frm_Sistema ventana = new Frm_Sistema();
            ventana.lbNombre.setText(busqueda_nombre);
            //mandar a llamar la sig ventana
            ventana.setVisible(true);
            this.dispose();
        } else {
            JOptionPane.showMessageDialog(this, "USUARIO NO REGISTRADO, FAVOR DE DARSE DE ALTA :3");
        }

    }//GEN-LAST:event_btentrarActionPerformed

