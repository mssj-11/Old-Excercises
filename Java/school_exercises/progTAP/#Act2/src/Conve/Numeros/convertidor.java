    private void convertirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_convertirActionPerformed

        if (decimal.isSelected() == true) {
            b.setText(jd.D_Binario(Integer.parseInt(d1.getText())));
            o.setText(String.valueOf(jd.D_Octal(Integer.parseInt(d1.getText()))));
            h.setText(String.valueOf(jd.D_Hexadecimal(Integer.parseInt(d1.getText()))));
        } else if (binario.isSelected() == true) {
            d.setText(String.valueOf(jd.Bn_B10(b1.getText(), 2)));

            int nuevo = jd.Bn_B10(b1.getText(), 2);
            o.setText(jd.D_Octal(nuevo));

            int nuev = jd.Bn_B10(b1.getText(), 2);
            h.setText(jd.D_Hexadecimal(nuev));
        } else if (octal.isSelected() == true) {
            d.setText(String.valueOf(jd.Bn_B10(ol.getText(), 8)));
            int ne = jd.Bn_B10(ol.getText(), 8);
            b.setText(jd.D_Binario(ne));
            int net = jd.Bn_B10(ol.getText(), 8);
            h.setText(jd.D_Hexadecimal(net));

        } else if (hexadecimal.isSelected() == true) {
            d.setText(String.valueOf(jd.Bn_B10(hl.getText(), 16)));
            int ne1 = jd.Bn_B10(hl.getText(), 16);
            b.setText(jd.D_Binario(ne1));
            int net1 = jd.Bn_B10(hl.getText(), 16);
            o.setText(jd.D_Octal(net1));

        }

        // TODO add your handling code here:
    }//GEN-LAST:event_convertirActionPerformed

