    public void metodo() {
        palillo[] p = new palillo[5];
        silla s = new silla();
        Filosofo[] f = new Filosofo[5];

        for (int i = 0; i < p.length; i++) {
            p[i] = new palillo(i);
        }
        for (int i = 0; i < f.length; i++) {
            f[i] = new Filosofo(i, p[i], p[(i + 1) % 5], s);
        }
        for (int i = 0; i < 5; i++) {
            f[i].start();

        }
    }

