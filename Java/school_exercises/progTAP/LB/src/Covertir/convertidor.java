    private void CalculadoraMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_CalculadoraMouseClicked
        // TODO add your handling code here:
        if (DE.getSelectedItem() == "Decimal" && A.getSelectedItem() == "Binario") {

            String resul = "";
            String F = C.BO(Integer.parseInt(n.getText()), 2);
            for (int i = F.length() - 1; i >= 0; i--) {
                resul = resul + F.charAt(i);
            }
            resl.setText(resul);

        } else if (DE.getSelectedItem() == "Decimal" && A.getSelectedItem() == "Octal") {

            String resul = "";
            String F = C.BO(Integer.parseInt(n.getText()), 8);
            for (int i = F.length() - 1; i >= 0; i--) {
                resul = resul + F.charAt(i);
            }
            resl.setText(resul);

        } else if (DE.getSelectedItem() == "Decimal" && A.getSelectedItem() == "Hexadecimal") {

            String resul = "";
            String F = C.HE(Integer.parseInt(n.getText()), 16);
            for (int i = F.length() - 1; i >= 0; i--) {
                resul = resul + F.charAt(i);
            }
            resl.setText(resul);

        } else if (DE.getSelectedItem() == "Decimal" && A.getSelectedItem() == "Decimal") {

            resl.setText(n.getText());

        } else if (DE.getSelectedItem() == "Binario" && A.getSelectedItem() == "Decimal") {

            int F = C.IBO(Integer.parseInt(n.getText()), 2);
            resl.setText(String.valueOf(F));

        } else if (DE.getSelectedItem() == "Binario" && A.getSelectedItem() == "Octal") {

            int nuevo = C.IBO(Integer.parseInt(n.getText()), 2);

            String resul = "";
            String F = C.BO(nuevo, 8);
            for (int i = F.length() - 1; i >= 0; i--) {
                resul = resul + F.charAt(i);
            }
            resl.setText(resul);

        } else if (DE.getSelectedItem() == "Binario" && A.getSelectedItem() == "Hexadecimal") {

            int nuevo = C.IBO(Integer.parseInt(n.getText()), 2);

            String resul = "";
            String F = C.HE(nuevo, 16);
            for (int i = F.length() - 1; i >= 0; i--) {
                resul = resul + F.charAt(i);
            }
            resl.setText(resul);

        } else if (DE.getSelectedItem() == "Binario" && A.getSelectedItem() == "Binario") {

            resl.setText(n.getText());

        } else if (DE.getSelectedItem() == "Octal" && A.getSelectedItem() == "Decimal") {

            int F = C.IBO(Integer.parseInt(n.getText()), 8);
            resl.setText(String.valueOf(F));

        } else if (DE.getSelectedItem() == "Octal" && A.getSelectedItem() == "Binaria") {

            int nuevo = C.IBO(Integer.parseInt(n.getText()), 8);

            String resul = "";
            String F = C.BO(nuevo, 2);
            for (int i = F.length() - 1; i >= 0; i--) {
                resul = resul + F.charAt(i);
            }
            resl.setText(resul);

        } else if (DE.getSelectedItem() == "Octal" && A.getSelectedItem() == "Hexadecimal") {

            int nuevo = C.IBO(Integer.parseInt(n.getText()), 8);

            String resul = "";
            String F = C.HE(nuevo, 16);
            for (int i = F.length() - 1; i >= 0; i--) {
                resul = resul + F.charAt(i);
            }
            resl.setText(resul);

        } else if (DE.getSelectedItem() == "Octal" && A.getSelectedItem() == "Octal") {

            resl.setText(n.getText());

        }

    }//GEN-LAST:event_CalculadoraMouseClicked

