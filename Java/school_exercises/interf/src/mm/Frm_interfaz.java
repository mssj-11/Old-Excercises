    void mostrardatosusuario(String valor) {
        DefaultTableModel ussuario = new DefaultTableModel();
        ussuario.addColumn("\t\tNombre");
        ussuario.addColumn("\tApellido");
        ussuario.addColumn("\t\tCorreo");
        ussuario.addColumn("\t\tContraseña");
        tbusuarios.setModel(ussuario);
        String sql = "";

        if (valor.equals("")) {
            sql = "SELECT *FROM usuarios";

        } else {
            sql = "SELECT *FROM usuarios WHERE nombre='" + valor + "'";

        }

        String[] datos = new String[4];

        try {
            Statement st = cn.createStatement();
            ResultSet rs = st.executeQuery(sql);

            while (rs.next()) {
                datos[0] = rs.getString(1);
                datos[1] = rs.getString(2);
                datos[2] = rs.getString(3);
                datos[3] = rs.getString(4);
                ussuario.addRow(datos);

            }
            tbusuarios.setModel(ussuario);

        } catch (SQLException ex) {
            Logger.getLogger(Frm_interfaz.class.getName()).log(Level.SEVERE, null, ex);
        }

    }

