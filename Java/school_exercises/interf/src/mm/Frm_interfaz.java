    private void gusuarioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_gusuarioActionPerformed
        // TODO add your handling code here:
        try {
            PreparedStatement psusuario = cn.prepareStatement("INSERT INTO usuarios(nombre,apellido,correo,contraseña) VALUES (?,?,?,?)");
            psusuario.setString(1, codnombre.getText());
            psusuario.setString(2, codapellido.getText());
            psusuario.setString(3, codcorreo.getText());
            psusuario.setString(4, codcontra.getText());

            psusuario.executeUpdate();
            mostrardatosusuario("");
        } catch (Exception e) {
            System.out.print(e.getMessage());
        }
    }//GEN-LAST:event_gusuarioActionPerformed

