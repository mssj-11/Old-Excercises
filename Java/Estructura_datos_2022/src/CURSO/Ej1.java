public class Ej1 {
    
    public static void main(String[] args) {
        
        //  Comparacion
        if(8 == 9)  System.out.println("8 es igual a nueve");
        if(9 < 4)  System.out.println("9 es menos a 4");
        if(0 > -4)  System.out.println("0 es mayor que -4");
        if(9 <= 20)  System.out.println("9 es menor o igual a 20");
        if(3 >= 9)  System.out.println("3 es mayor o igual a 9");
        if(6 != 9)  System.out.println("6 es diferente de 9");
        
    }
}